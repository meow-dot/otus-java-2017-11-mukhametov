package orm.base;

public class UserDataSet extends DataSet{

    protected final String name;
    protected final int age;

    public UserDataSet(long id, String name, int age) {
        super(id);
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
